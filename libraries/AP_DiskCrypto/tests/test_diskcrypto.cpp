TEST(AP_DiskCrypto, RoundTripManySectors)
{
    AP_DiskCrypto crypto;
    uint8_t key[AP_DISKCRYPTO_KEY_LEN];
    for (uint8_t i = 0; i < AP_DISKCRYPTO_KEY_LEN; i++) {
        key[i] = uint8_t(0xA5 ^ (i * 3));
    }
    ASSERT_TRUE(crypto.init(key));

    const uint64_t lbas[] = { 0, 1, 63, 2048, 0x0001020304ULL, 0xFFFFFFFFFFFFFFFFULL };
    for (uint8_t k = 0; k < ARRAY_SIZE(lbas); k++) {
        uint8_t pt[AP_DiskCrypto::SECTOR_SIZE];
        for (uint16_t i = 0; i < sizeof(pt); i++) {
            pt[i] = uint8_t((i ^ (lbas[k] & 0xff)) + k);
        }
        uint8_t ct[AP_DiskCrypto::SECTOR_SIZE];
        ASSERT_TRUE(crypto.encrypt_sector(pt, ct, lbas[k]));
        EXPECT_NE(0, memcmp(ct, pt, sizeof(ct)));   // ciphertext must differ from plaintext
        ASSERT_TRUE(crypto.decrypt_sector(ct, lbas[k]));
        EXPECT_EQ(0, memcmp(ct, pt, sizeof(ct)));
    }
}

TEST(AP_DiskCrypto, TweakDependsOnLBA)
{
    AP_DiskCrypto crypto;
    uint8_t key[AP_DISKCRYPTO_KEY_LEN];
    fill_kat_key(key);
    ASSERT_TRUE(crypto.init(key));

    uint8_t pt[AP_DiskCrypto::SECTOR_SIZE];
    memset(pt, 0, sizeof(pt));

    uint8_t ct0[AP_DiskCrypto::SECTOR_SIZE];
    uint8_t ct1[AP_DiskCrypto::SECTOR_SIZE];
    ASSERT_TRUE(crypto.encrypt_sector(pt, ct0, 0));
    ASSERT_TRUE(crypto.encrypt_sector(pt, ct1, 1));
    // identical plaintext, adjacent LBAs => different ciphertext
    EXPECT_NE(0, memcmp(ct0, ct1, sizeof(ct0)));

    // decrypting with the wrong LBA must not recover the plaintext
    uint8_t wrong[AP_DiskCrypto::SECTOR_SIZE];
    memcpy(wrong, ct0, sizeof(wrong));
    ASSERT_TRUE(crypto.decrypt_sector(wrong, 1));
    EXPECT_NE(0, memcmp(wrong, pt, sizeof(wrong)));
}

TEST(AP_DiskCrypto, RejectsUninitialised)
{
    AP_DiskCrypto crypto;
    uint8_t pt[AP_DiskCrypto::SECTOR_SIZE] = {};
    uint8_t ct[AP_DiskCrypto::SECTOR_SIZE];
    EXPECT_FALSE(crypto.encrypt_sector(pt, ct, 0));
    EXPECT_FALSE(crypto.decrypt_sector(pt, 0));
}

TEST(AP_DiskCrypto, RejectsDuplicatedKey)
{
    // XTS requires the data key and tweak key (the two 32-byte halves) to
    // differ; init() must reject a key whose halves are identical and stay
    // uninitialised, matching the OpenSSL-based host recovery tool.
    AP_DiskCrypto crypto;
    uint8_t key[AP_DISKCRYPTO_KEY_LEN];
    for (uint8_t i = 0; i < AP_DISKCRYPTO_KEY_LEN; i++) {
        key[i] = uint8_t(i & 31);   // bytes 0..31 repeated => both halves equal
    }
    EXPECT_FALSE(crypto.init(key));
    EXPECT_FALSE(crypto.initialised());

    // and a key with distinct halves still initialises
    key[0] ^= 0xff;
    EXPECT_TRUE(crypto.init(key));
    EXPECT_TRUE(crypto.initialised());
}

